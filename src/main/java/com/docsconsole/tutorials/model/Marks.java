package com.docsconsole.tutorials.model;

public class Marks {
    private Long firstLang;
    private Long secondLang;
    private Long mathematics;
    private Long generalScience;
    private Long socialStudies;

    public Long getFirstLang() {
        return firstLang;
    }

    public void setFirstLang(Long firstLang) {
        this.firstLang = firstLang;
    }

    public Long getSecondLang() {
        return secondLang;
    }

    public void setSecondLang(Long secondLang) {
        this.secondLang = secondLang;
    }

    public Long getMathematics() {
        return mathematics;
    }

    public void setMathematics(Long mathematics) {
        this.mathematics = mathematics;
    }

    public Long getGeneralScience() {
        return generalScience;
    }

    public void setGeneralScience(Long generalScience) {
        this.generalScience = generalScience;
    }

    public Long getSocialStudies() {
        return socialStudies;
    }

    public void setSocialStudies(Long socialStudies) {
        this.socialStudies = socialStudies;
    }
}
